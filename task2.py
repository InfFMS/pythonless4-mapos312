# Напишите функцию number_to_words(num), которая принимает
# в качестве аргумента натуральное число num и возвращает
# его словесное описание на русском языке.
#
# Примечание 1.
# Считайте, что число 1 ≤ num ≤ 99.
#
# Примечание 2.
# Следующий программный код:
#
# print(number_to_words(7))
# print(number_to_words(85))
# должен выводить:
#
# семь
# восемьдесят пять
def a(n):
    if not (1 <= n <= 99):
        return "Число должно быть в диапазоне от 1 до 99."

    units = ["", "один", "два", "три", "четыре", "пять",
             "шесть", "семь", "восемь", "девять"]
    teens = ["десять", "одиннадцать", "двенадцать", "тринадцать",
             "четырнадцать", "пятнадцать", "шестнадцать",
             "семнадцать", "восемнадцать", "девятнадцать"]
    tens = ["", "", "двадцать", "тридцать", "сорок",
            "пятьдесят", "шестьдесят", "семьдесят",
            "восемьдесят", "девяносто"]

    if n < 10:
        return units[n]
    elif n < 20:
        return teens[n - 10]
    else:
        ten = n // 10
        unit = n % 10
        return tens[ten] + (" " + units[unit] if unit > 0 else "")